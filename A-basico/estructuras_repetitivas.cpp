#include<iostream>
using namespace std;

//inicio de programa
int main(){

    //el bucle for
    for(int i=0;i<5;i++){
        cout<<"** **"<<endl;
    }
    //el bucle while
    int j = 5;
    int contador = 0;
    while(j>contador){
        cout<<"** **"<<endl;
        contador = contador + 1;
    }


    //fin de programa
    return 0;
 }